public class Bracketless2 {
    public static void main(String[] args) {
        int i = 0;

        // While loop runs while i < 10
        while (i < 10) {
            // If i > 1, print the message
            if (i > 1)
                System.out.println("Greater than " + i);

            // Increment i after the if block
            i++;
        }
    }
}
