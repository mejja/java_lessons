public class CharFromStringExample {
    public static void main(String[] args) {
        // Create a String with a single character
        String stringValue = "E";

        // Convert String to Character using valueOf(String s)
        Character charValue = Character.valueOf(stringValue);

        // Print the results
        System.out.println("String value is: " + stringValue);
        System.out.println("Character value is: " + charValue);
    }
}
