public class BooleanToString {
    public static void main(String[] args) {
        //Boolean instance
        Boolean booleanValue = true;

        // Convertion using Boolean.toString() method
        String stringValue = Boolean.toString(booleanValue);

        System.out.println("The Boolean value is: " + booleanValue);//boolean
        System.out.println("The String value is: " + stringValue);//string value
    }
}
